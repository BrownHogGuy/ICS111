public class ComparisonDemo {
	/**
	 * Main Method
	 * no args used...
	 */
	public static void main(String[] args) {
		int num1 = 78;
		boolean bool;
		bool = num1 == num1;
		System.out.println(bool);
		bool = num1 <= num1;
		System.out.println(bool);
		bool = num1 >= num1;
		System.out.println(bool);
		bool = num1 > num1;
		System.out.println(bool);
		bool = num1 < num1;
		System.out.println(bool);
		bool = num1 != num1;
		System.out.println(bool);
	}
}
